/**
 * 공통 모듈 저장
 */

/** sum(number1, number2) */
export function sum(number1, number2) {
    return parseInt(number1) + parseInt(number2);
}

/** sub(number1, number2) */
export function sub(number1, number2) {
    return parseInt(number1) - parseInt(number2);
}

/** mul(number1, number2) */
export function mul(number1, number2) {
    return parseInt(number1) * parseInt(number2);
}

/** div(number1, number2) */
export function div(number1, number2) {
    return parseInt(number1) / parseInt(number2);
}